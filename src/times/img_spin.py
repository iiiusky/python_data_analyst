#### 图片旋转
#### 对目标文件夹下的图片文件进行循环旋转

import cv2
import glob

img_list = glob.glob(r'/Users/wangguoqiang/Desktop/*.png')

for i in img_list:
    img = cv2.imread(i)
    new_img = cv2.flip(img, -1)   # 旋转类型
    cv2.imwrite(i, new_img)
